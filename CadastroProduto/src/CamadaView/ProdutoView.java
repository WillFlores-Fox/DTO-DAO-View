    private void BntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntActionPerformed
        ProdutoDTO objProdutodto = new ProdutoDTO();
        
        String nome, preco, quantidade;
        
        nome = Nometxt.getText();
        preco = Precotxt.getText();
        quantidade = Qnttxt.getText();
        
        objProdutodto.setNome(nome);
        objProdutodto.setPreco(Double.parseDouble(preco));
        objProdutodto.setQuantidade(Integer.parseInt(quantidade));
        
        ProdutoDAO objProdutodao = new ProdutoDAO();
        
        objProdutodao.SalvarProduto(objProdutodto);
    }//GEN-LAST:event_BntActionPerformed

